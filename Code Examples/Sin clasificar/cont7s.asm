CODIGO SEGMENT
  ASSUME CS:CODIGO, DS:DATOS

INICIO:	MOV AX, DATOS
	MOV DS, AX

CERO:	MOV CL, 0

INIC2:	CMP CL, 16
	JE CERO
	CALL VISUAL
	CALL RETARD
	INC CL
	JMP INIC2

VISUAL:	MOV DX, OFFSET TABLA
	MOV SI, DX
	MOV BL, CL
	MOV BH, 0
	MOV AL, BYTE PTR[SI+BX]
	MOV DX, 378H
	OUT DX, AL
	RET

RETARD:	PUSH AX
	PUSH BX
	PUSH CX
	MOV AX, 1000
P3:	MOV BX, 1000
P2:	MOV CX, 300
P1:	DEC CX
	JNZ P1
	DEC BX
	JNZ P2
	DEC AX
	JNZ P3
	POP CX
	POP BX
	POP AX
	RET

	CODIGO ENDS

END INICIO
