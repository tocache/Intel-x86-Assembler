DATOS SEGMENT
	L1 DB "********************", 0AH, 0DH, "$"
	L2 DB "*                  *", 0AH, 0DH, "$"
DATOS ENDS

CODIGO SEGMENT
  ASSUME CS:CODIGO, DS:DATOS

INICIO:
	MOV AX, DATOS
	MOV DS, AX
	LEA DX, L1
	MOV AH, 09H
	INT 21H
	CALL RETARDO

	MOV CX, 18
INICIO2:
	LEA DX, L2
	MOV AH, 09H
	INT 21H
	CALL RETARDO
	LOOP INICIO2

INICIO3:
	LEA DX, L1
	MOV AH, 09H
	INT 21H
	JMP FIN

RETARDO:
	PUSH DX
	PUSH CX
	PUSH BX
	MOV DX, 1000
P1:	MOV CX, 1000
P2:	MOV BX, 200
P3:	DEC BX
	JNZ P3
	DEC CX
	JNZ P2
	DEC DX
	JNZ P1
	POP BX
	POP CX
	POP DX
	RET

FIN:
	MOV AH, 4CH
	INT 21H

CODIGO ENDS
END INICIO
END INICIO2
END INICIO3
END RETARDO
END P1
END P2
END P3
END FIN
