DATOS SEGMENT
DATOS ENDS

CODIGO SEGMENT
  ASSUME CS:CODIGO, DS:DATOS

INICIO:
	MOV DX, 378H
	MOV AL, 00H
	OUT DX, AL
	CALL RETA
	MOV DX, 378H
	MOV AL, 01H
	OUT DX, AL
	CALL RETA
	MOV DX, 378H
	MOV AL, 02H
	OUT DX, AL
	CALL RETA
	MOV DX, 378H
	MOV AL, 03H
	OUT DX, AL
	CALL RETA
	MOV DX, 378H
	MOV AL, 02H
	OUT DX, AL
	CALL RETA
	MOV DX, 378H
	MOV AL, 01H
	OUT DX, AL
	CALL RETA
	JMP INICIO


RETA:	PUSH DX
	PUSH CX
	PUSH BX
	MOV DX, 1000
P1:	MOV CX, 1000
P2:	MOV BX, 300
P3:	DEC BX
	JNZ P3
	DEC CX
	JNZ P2
	DEC DX
	JNZ P1
	POP BX
	POP CX
	POP DX
	RET

FIN:	MOV AH, 4CH
	INT 21H


CODIGO ENDS

END INICIO
