CODIGO SEGMENT
  ASSUME CS:CODIGO, DS:DATOS

INICIO:	MOV AX, DATOS
	MOV DS, AX
INIC2:	MOV AH, 01H
	INT 21H
	CMP AL, 51H
	JE FIN
	CMP AL, 30H
	JAE LIM1
	JMP INIC2

LIM1:	CMP AL, 39H
	JBE LIM2
	JMP LIM3

LIM2:	SUB AL, 30H		;He validado el primer rango
	JMP VISUAL

LIM3:	CMP AL, 41H
	JAE LIM4
	JMP INIC2

LIM4:	CMP AL, 46H
	JBE LIM5
	JMP INIC2

LIM5:	SUB AL, 37H		;He validado el segundo rango
	JMP VISUAL

VISUAL:	MOV DX, OFFSET TABLA
	MOV SI, DX
	MOV BL, AL
	MOV BH, 0
	MOV AL, BYTE PTR[SI+BX]
	MOV DX, 378H
	OUT DX, AL
	JMP INIC2

FIN:	MOV AH, 4CH
	INT 21H

	CODIGO ENDS

END INICIO
