CODIGO SEGMENT
ASSUME CS:CODIGO

INICIO:
	MOV DX, 378H
	MOV AL, 00H
	OUT DX, AL
	CALL RETARDO
	MOV AL, 01H
	OUT DX, AL
	CALL RETARDO
	JMP INICIO

RETARDO:PUSH DX
	PUSH CX
	PUSH BX
	MOV DX, 1000
P1:	MOV CX, 1000
P2:	MOV BX, 200
P3:	DEC BX
	JNZ P3
	DEC CX
	JNZ P2
	DEC DX
	JNZ P1
	POP BX
	POP CX
	POP DX
	RET



;Para finalizar el programa correctamente
	MOV AH, 4CH
	INT 21H

	CODIGO ENDS

END INICIO
