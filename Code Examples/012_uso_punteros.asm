DATOS SEGMENT
  CADE1 DB "JOAO"
  CADE2 DB "ANGEL"
DATOS ENDS

CODIGO SEGMENT
  ASSUME CS:CODIGO, DS:DATOS

INICIO:	MOV AX, DATOS
	MOV DS, AX
	LEA DX, CADE2
	MOV SI, DX
	MOV BL, BYTE PTR[SI+4]
	MOV AH, 02H
	MOV DL, BL
	INT 21H
	LEA DX, CADE1
	MOV SI, DX
	MOV BL, BYTE PTR[SI+1]
	MOV AH, 02H
	MOV DL, BL
	INT 21H

FIN:	MOV AH, 4CH
	INT 21H

	CODIGO ENDS

END INICIO
