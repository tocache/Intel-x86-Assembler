
DATOS SEGMENT
  TIT1 DB "---------"
  TIT2 DB "|--   -- |"
  TIT3 DB "|   |     |"
  TIT4 DB "| \--/  |"
  TIT5 DB "---------"

DATOS ENDS

CODIGO SEGMENT
  ASSUME CS:CODIGO, DS:DATOS

INICIO:	MOV AX, DATOS
	MOV DS, AX
	MOV AH, 09H
	LEA DX, TIT1
	INT 21H
	CALL NXT_LN
	LEA DX, TIT2
	INT 21H
	CALL NXT_LN
	LEA DX, TIT3
	INT 21H
	CALL NXT_LN
	LEA DX, TIT4
	INT 21H
	CALL NXT_LN
	LEA DX, TIT5
	INT 21H
	JMP FIN

NXT_LN:	PUSHA
	MOV AH, 02H
	MOV DL, 0AH
	INT 21H
	MOV DL, 0DH
	INT 21H
	POPA
	RET

FIN:	MOV AH, 4CH
	INT 21H

	CODIGO ENDS
END INICIO
